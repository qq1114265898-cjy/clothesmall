<template>
    <div class="tabcontrol">
        <div v-for="(item,index) in titles" :key="item.id" class="tabcontrol-item" :class="{active:currentIndex==index}" @click="itemClick(index)">
            <span>{{item}}</span>
        </div>
    </div>
</template>
<script>
  export default {
  name:'',
  props:{
      titles:{
          type:Array,
          default(){
              return[]
          }
      }
  },
  data(){
      return {
          currentIndex:0
      }
  },
  methods: {
      itemClick(index){
          this.currentIndex=index
      }
  },
  }
</script>

